package es.martinezpenya.ejemplos.UD07.P4_Comparadores.Comparator1;

public class Objeto {

    public int a;
    public int b;

    public Objeto(int a, int b) {
        this.a = a;
        this.b = b;
    }

    @Override
    public String toString() {
        return "Objeto{" + "a=" + a + ", b=" + b + '}';
    }

}
